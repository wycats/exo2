fn is_git_worktree(root: &std::path::Path) -> Result<bool, std::io::Error> {
    let output = std::process::Command::new("git")
        .arg("-C")
        .arg(root)
        .arg("rev-parse")
        .arg("--is-inside-work-tree")
        .output()?;

    Ok(output.status.success() && String::from_utf8_lossy(&output.stdout).trim() == "true")
}

fn git_config_get(root: &std::path::Path, key: &str) -> Result<Option<String>, std::io::Error> {
    let output = std::process::Command::new("git")
        .arg("-C")
        .arg(root)
        .arg("config")
        .arg("--local")
        .arg("--get")
        .arg(key)
        .output()?;

    if output.status.success() {
        Ok(Some(
            String::from_utf8_lossy(&output.stdout).trim().to_string(),
        ))
    } else {
        Ok(None)
    }
}

fn git_config_set(root: &std::path::Path, key: &str, value: &str) -> Result<(), std::io::Error> {
    let status = std::process::Command::new("git")
        .arg("-C")
        .arg(root)
        .arg("config")
        .arg("--local")
        .arg(key)
        .arg(value)
        .status()?;

    if status.success() {
        Ok(())
    } else {
        Err(std::io::Error::other(format!(
            "git config failed for {key}"
        )))
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn merge_driver_detection_uses_the_last_repeated_section_value() {
        let temp = tempfile::tempdir().expect("create tempdir");
        let repo = temp.path();
        let status = std::process::Command::new("git")
            .arg("init")
            .arg(repo)
            .status()
            .expect("run git init");
        assert!(status.success());

        let config_path = repo.join(".git/config");
        let mut config = std::fs::read_to_string(&config_path).expect("read config");
        config.push_str(
            "\n[merge \"exo-sql-dump\"]\n\tdriver = false\n[merge \"exo-sql-dump\"]\n\tdriver = true\n",
        );
        std::fs::write(&config_path, config).expect("write config");

        assert!(sql_dump_merge_driver_configured(repo).expect("read merge driver"));
    }
}
