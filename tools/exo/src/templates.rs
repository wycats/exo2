/// Install exo instruction files to .github/instructions/
/// These are automatically injected by VS Code alongside user's copilot-instructions.md
pub fn install_instructions(root: &std::path::Path) -> Result<usize, std::io::Error> {
    let instructions_dir = root.join(".github/instructions");
    std::fs::create_dir_all(&instructions_dir)?;

    let mut written = 0usize;
    for instruction in INSTRUCTION_TEMPLATES {
        let path = instructions_dir.join(instruction.filename);
        if write_if_changed(&path, instruction.contents)? {
            written += 1;
        }
    }

    Ok(written)
}

pub fn install_project_prompts(root: &std::path::Path) -> Result<usize, std::io::Error> {
    let prompts_dir = root.join(".github/prompts");
    std::fs::create_dir_all(&prompts_dir)?;

    let mut written = 0usize;
    for prompt in PROJECT_PROMPTS {
        let path = prompts_dir.join(prompt.filename);
        // Create parent directories for nested prompts (e.g., coherence/check-*.prompt.md)
        if let Some(parent) = path.parent() {
            std::fs::create_dir_all(parent)?;
        }
        if write_if_changed(&path, prompt.contents)? {
            written += 1;
        }
    }

    Ok(written)
}

fn write_if_changed(path: &std::path::Path, contents: &str) -> Result<bool, std::io::Error> {
    let existing = std::fs::read_to_string(path).ok();
    if existing.as_deref() == Some(contents) {
        return Ok(false);
    }

    std::fs::write(path, contents)?;
    Ok(true)
}

// Request-scoped override used by daemon-routed CLI updates.
thread_local! {
    static GLOBAL_PROMPTS_HOME_OVERRIDE: std::cell::RefCell<Option<Option<std::path::PathBuf>>> =
        const { std::cell::RefCell::new(None) };
}

struct GlobalPromptsHomeOverrideGuard(Option<Option<std::path::PathBuf>>);

impl Drop for GlobalPromptsHomeOverrideGuard {
    fn drop(&mut self) {
        GLOBAL_PROMPTS_HOME_OVERRIDE.with(|home| {
            home.replace(self.0.take());
        });
    }
}

pub(crate) fn with_global_prompts_home_override<T>(
    home: Option<Option<std::path::PathBuf>>,
    operation: impl FnOnce() -> T,
) -> T {
    let previous = GLOBAL_PROMPTS_HOME_OVERRIDE.with(|current| current.replace(home));
    let _guard = GlobalPromptsHomeOverrideGuard(previous);
    operation()
}

/// Install global prompts to the user's VS Code prompts directory.
/// Path: `~/.config/Code/User/prompts/exo/`
pub fn install_global_prompts() -> Result<usize, std::io::Error> {
    let home =
        GLOBAL_PROMPTS_HOME_OVERRIDE.with(|override_home| match &*override_home.borrow() {
            Some(Some(home)) => Ok(home.clone()),
            Some(None) => Err(std::io::Error::new(
                std::io::ErrorKind::NotFound,
                "HOME environment variable not set for update caller",
            )),
            None => std::env::var("HOME")
                .map(std::path::PathBuf::from)
                .map_err(|_| {
                    std::io::Error::new(
                        std::io::ErrorKind::NotFound,
                        "HOME environment variable not set",
                    )
                }),
        })?;

    let global_prompts_dir = home.join(".config/Code/User/prompts/exo");
    std::fs::create_dir_all(&global_prompts_dir)?;

    let mut written = 0usize;
    for prompt in PROJECT_PROMPTS {
        let path = global_prompts_dir.join(prompt.filename);
        // Create parent directories for nested prompts
        if let Some(parent) = path.parent() {
            std::fs::create_dir_all(parent)?;
        }
        if write_if_changed(&path, prompt.contents)? {
            written += 1;
        }
    }

    Ok(written)
}

